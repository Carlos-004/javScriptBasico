//Comparar 2 valores

const numero = 20;
const numeroDos = "20";
const numeroTres = 30;


//Ver si 2 numeros son iguales
console.log(numero == numeroTres);
console.log(numero == numeroDos);

//Comparador stricto
console.log(numero === numeroTres);
console.log(numero === numeroDos);
console.log(numero === parseInt(numeroDos));


//Diferentes

console.log(numero !== numeroTres);
console.log(numero != numeroDos);
console.log(numero !== numeroDos);