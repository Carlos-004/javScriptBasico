//Metodo de los string de convertir a mayuscula y minusculas

const producto = "Monitor de 24 pulgadas";
console.log(producto)

//El metodo toLowerCase() ayuda a comvertir a minuscula
console.log(producto.toLowerCase());//monitor de 24 pulgadas

//El metodo toUpperCase() ayuda a comvertir a mayuscula
console.log(producto.toUpperCase());//MONITOR DE 24 PULGADAS